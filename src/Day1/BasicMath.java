package Day1;

public class BasicMath {
	public static void main(String args[]) {
		int a=10, b=20, c, d, e, f; //declaration=c,d,e,f, and a,b are both declared and initialization
		
		c=a+b; //Expression and ; means end of statement
		System.out.println(c);
		
		d=a-b;
		System.out.println(d);
		
		e=a*b;
		System.out.println(e);
		
		f=a/b;
		System.out.println(f);
		
	}

}
